    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String nombre, telefono, numero_cuenta, direccion, correo_electronico;
        if(!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField4.getText().equals("")  && !jTextField5.getText().equals("") )
        {
                nombre=jTextField1.getText();
                telefono=jTextField2.getText();
                numero_cuenta=jTextField4.getText();
                direccion=jTextField5.getText();
                correo_electronico=jTextField6.getText();
                Consultas_SQL c = new Consultas_SQL();
                if(!actualizar_tabla){
                   c.Reg_Proveedor(nombre, telefono, numero_cuenta, direccion, correo_electronico);
                   JOptionPane.showMessageDialog(null, "Datos ingresados correctamente", "Registro completo", 1);   
                }else{
                    c.Actualizar_Proveedor(nombre, telefono, numero_cuenta, direccion, correo_electronico,id_proveedor);
                    pro.crearTabla();
                    JOptionPane.showMessageDialog(null, "Datos actualizados correctamente", "Actualizacion completa", 1); 
                }
                c.Desconectar();
                                              
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese los datos obligatorios", "Mensaje de error", 0);
        }
    }//GEN-LAST:event_jButton2MouseClicked

